OUTPUT_DIR = Path("VARKsound")
GEMINI_API_KEY = os.environ.get("GEMINI_API_KEY")
GEMINI_URL = (
    "https://generativelanguage.googleapis.com/v1beta/models/"
    f"gemini-2.5-flash-preview-tts:generateContent?key={GEMINI_API_KEY}"
)


def build_text(idx: int, q: dict) -> str:
    """สร้างข้อความเต็มสำหรับแต่ละข้อ"""
    opts = " ".join(f"ตัวเลือก{label} {text}" for label, text in q["options"])
    return f"ข้อที่ {idx + 1} {q['text']} {opts}"


def pcm_to_wav(raw_bytes: bytes, sample_rate: int = 24000) -> bytes:
    """แปลง raw PCM 16-bit mono → WAV"""
    header = struct.pack(
        "<4sI4s4sIHHIIHH4sI",
        b"RIFF", 36 + len(raw_bytes), b"WAVE",
        b"fmt ", 16, 1, 1,
        sample_rate, sample_rate * 2, 2, 16,
        b"data", len(raw_bytes),
    )
    return header + raw_bytes


def generate_audio(text: str) -> bytes:
    """เรียก Gemini TTS แล้วคืน WAV bytes"""
    payload = {
        "contents": [{"parts": [{"text": text}]}],
        "generationConfig": {
            "responseModalities": ["AUDIO"],
            "speechConfig": {
                "voiceConfig": {
                    "prebuiltVoiceConfig": {"voiceName": "Aoede"}
                }
            },
        },
    }
    resp = httpx.post(GEMINI_URL, json=payload, timeout=60)
    resp.raise_for_status()

    data      = resp.json()
    part      = data["candidates"][0]["content"]["parts"][0]["inlineData"]
    b64_audio = part["data"]
    mime_type = part.get("mimeType", "audio/L16;rate=24000")

    raw = base64.b64decode(b64_audio)

    if "L16" in mime_type or "pcm" in mime_type.lower():
        rate_m      = re.search(r"rate=(\d+)", mime_type)
        sample_rate = int(rate_m.group(1)) if rate_m else 24000
        return pcm_to_wav(raw, sample_rate)

    return raw  # WAV / MP3 อื่นๆ → ส่งตรง


def main():
    if not GEMINI_API_KEY:
        print("❌ ไม่พบ GEMINI_API_KEY ใน .env")
        return

    OUTPUT_DIR.mkdir(exist_ok=True)
    print(f"📁 Output: {OUTPUT_DIR.resolve()}\n")

    for i, q in enumerate(QUESTIONS):
        out_path = OUTPUT_DIR / f"sound{i + 1}.wav"

        # ข้ามถ้ามีไฟล์อยู่แล้ว (รัน regenerate เฉพาะที่ต้องการ)
        if out_path.exists():
            print(f"  ⏭  sound{i + 1}.wav มีอยู่แล้ว — ข้าม")
            continue

        text = build_text(i, q)
        print(f"  🔊 Generating sound{i + 1}.wav ...", end=" ", flush=True)
        try:
            wav = generate_audio(text)
            out_path.write_bytes(wav)
            print(f"✅ ({len(wav):,} bytes)")
        except Exception as e:
            print(f"❌ {e}")

    print("\n✨ เสร็จแล้ว! ไฟล์เสียงอยู่ใน VARKsound/")


if __name__ == "__main__":
    main()
